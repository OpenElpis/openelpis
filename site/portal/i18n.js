  function detect() {
    try { var s = localStorage.getItem("elpis_lang"); if (s && I18N[s]) return s; } catch (e) {}
    var n = (navigator.languages && navigator.languages[0]) || navigator.language || "en";
    var c = n.toLowerCase().split("-")[0];
    return I18N[c] ? c : "en";
  }
  function apply(lang, persist) {
    var d = I18N[lang] || I18N.en;
    document.documentElement.lang = lang;
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var v = d[el.getAttribute("data-i18n")]; if (v != null) el.textContent = v;
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(function (el) {
      var v = d[el.getAttribute("data-i18n-ph")]; if (v != null) el.setAttribute("placeholder", v);
    });
    if (d._title) document.title = d._title;
    // Persist ONLY a manual choice. Auto-detect (browser language) must not be saved,
    // so a real manual choice always wins and is never overwritten by detection.
    if (persist) { try { localStorage.setItem("elpis_lang", lang); } catch (e) {} }
    var sw = document.getElementById("portalLang"); if (sw) sw.value = lang;
    window.OE_T = d; window.OE_LANG = lang;
    if (typeof window.onPortalLang === "function") window.onPortalLang(d);
  }
